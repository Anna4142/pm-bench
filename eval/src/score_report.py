"""Render a Markdown REPORT.md from baseline outputs in eval/output/."""

from __future__ import annotations

import os
from pathlib import Path

import pandas as pd


def _df_md(df: pd.DataFrame) -> str:
    """Render a small DataFrame as a GitHub-flavored Markdown table without tabulate."""
    if df.empty:
        return "_(empty)_"

    def fmt(v: object) -> str:
        if isinstance(v, float):
            return f"{v:.4f}"
        return str(v)

    cols = [str(c) for c in df.columns]
    head = "| " + " | ".join(cols) + " |"
    sep = "| " + " | ".join(["---"] * len(cols)) + " |"
    body = "\n".join(
        "| " + " | ".join(fmt(v) for v in row) + " |"
        for row in df.itertuples(index=False, name=None)
    )
    return "\n".join([head, sep, body])


def main(out_dir: Path) -> Path:
    summary = pd.read_csv(out_dir / "baseline_summary.csv")
    by_bucket = pd.read_csv(out_dir / "baseline_summary_by_bucket.csv")
    eval_index = pd.read_parquet(out_dir / "eval_index.parquet")
    filtered = pd.read_parquet(out_dir / "markets_filtered.parquet")

    bucket_counts = (
        eval_index.groupby("bucket").size().rename("n").reset_index()
    )

    report = []
    report.append("# Eval Report\n")
    report.append("Generated by `eval/src/pipeline.py`.\n")
    report.append("## Corpus\n")
    report.append(
        f"- Filtered finalized markets (yes_ask 1..99, volume_24h>0): "
        f"**{len(filtered):,}**\n"
        f"- Eval slice: **{len(eval_index):,}** markets across "
        f"**{eval_index['bucket'].nunique()}** price buckets\n"
    )
    report.append("### Eval bucket sizes\n")
    report.append(_df_md(bucket_counts))
    report.append("\n\n## Baselines (mean PnL is $ per contract per market)\n")
    report.append(_df_md(summary))
    report.append("\n\n## Baselines per price bucket\n")
    pivot = by_bucket.pivot(index="bucket", columns="policy", values="mean_pnl")
    report.append(_df_md(pivot.reset_index()))
    report.append("\n\n## Notes\n")
    report.append(
        "- **hold**: never trades; PnL is exactly 0. Useful as the no-action floor.\n"
        "- **always_yes / always_no**: probe directional bias of the eval split.\n"
        "- **price_as_prob**: assumes the market is calibrated; near-zero edge by construction.\n"
        "- **ev_greedy**: uses calibration learned on the time-prior train half "
        "(`calibration_curve.csv`). This is the strongest non-LLM baseline; the LLM should beat it.\n"
    )

    out = out_dir / "REPORT.md"
    out.write_text("".join(report))
    print(f"score_report: wrote {out}")
    return out


if __name__ == "__main__":
    here = Path(__file__).resolve().parents[2]
    main(Path(os.environ.get("EVAL_OUT_DIR", here / "eval" / "output")))
